<p>
  However, when breaking down the occupations by race and gender, we can see clear delineations of
  who works in which jobs. This over- or underrepresentation of race and gender groups in
  occupations despite similar levels of education—and taking into account what the role requires—is
  called <a
    href="https://econpapers.repec.org/article/eejeeconj/v_3a1_3ay_3a1974_3ai_3a2_3ap_3a103-110.htm"
    target="_blank">occupational crowding</a
  >. To measure its prevalence across occupations, we used a
  <a href="https://www.epi.org/publication/whiter_jobs_higher_wages/" target="_blank"
    >three-tiered crowding index</a
  >.
</p>
<div class="crowding-list">
  <p>
    <span class="highlight-p">Proportional crowding</span> indicates that the share of educational attainment
    required for an occupation among a race and gender group roughly mirrors the share of workers in
    that occupation.
  </p>
  <p>
    <span class="highlight-ci">Crowded in</span> indicates that the share of workers in an occupation
    is higher than the share that have the required education.
  </p>
  <p>
    <span class="highlight-co">Crowded out</span> indicates that the share of workers in an occupation
    is lower than the share that have the required education.
  </p>
</div>
<p>
  If we compare electricians and food and beverage workers using these categories, we see that among
  the share of people who hold the requisite education, white men are overrepresented among
  electricians and underrepresented among food and beverage workers. For white, Black, and Latina
  women, the opposite is true.
</p>

